    private final static String GOODS_6S_PRICE_URL = "https://merrige.hyxmt.cn/ajax/bussiness.ashx?action=product_oms_price";

    public static class Goods6sPrice {
        @JSONField(name = "sjbm")
        private String goodsCode;
        private String price;

        public String getGoodsCode() {
            return goodsCode;
        }

        public void setGoodsCode(String goodsCode) {
            this.goodsCode = goodsCode;
        }

        public String getPrice() {
            return price;
        }

        public void setPrice(String price) {
            this.price = price;
        }

        @Override
        public String toString() {
            return "Goods6sPrice{" +
                    "goodsCode='" + goodsCode + '\'' +
                    ", price='" + price + '\'' +
                    '}';
        }
    }

    /**
     * 根据商家编码获取经销商系统商品6s价格
     *
     * @param type          品牌标识
     * @param goodsCodeList 商家编码集合
     * @return 商家编码对应6s价格
     */
    public static List<Goods6sPrice> getGoods6sPriceList(Integer type, List<String> goodsCodeList) {
        Map<String, Object> map = new HashMap<>();
        map.put("type", type);
        map.put("product_codes", goodsCodeList);
        String result =  HttpUtil.doPost2(GOODS_6S_PRICE_URL, JSONObject.parseObject(JSON.toJSONString(map)));
        if (StringUtils.isEmpty(result)) {
            return new ArrayList<>();
        }
        return JSONArray.parseArray(result, Goods6sPrice.class);
    }

